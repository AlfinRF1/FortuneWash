private void tampilkanData() {
     DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    
    String sql = "SELECT * FROM transaksi_beli";
    
    try (Connection conn = DatabaseManager.getConnection();
         PreparedStatement stmt = conn.prepareStatement(sql);
         ResultSet rs = stmt.executeQuery()) {
         
        while (rs.next()) {
            Object[] row = {
                rs.getString("kode_pembelian"),
                rs.getString("tanggal_pembelian"),
                rs.getString("No_Ktp_supplier"),
                rs.getString("Id_barang")
            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error menampilkan data: " + e.getMessage());
    }
}

private boolean isValidDate(String date) {
    try {
        // Pastikan format tanggal sesuai dengan yyyy-MM-dd
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        sdf.setLenient(false); // Jangan izinkan tanggal yang tidak valid (misal 31-02-2022)
        sdf.parse(date);
        return true;
    } catch (java.text.ParseException e) {
        return false;
    }
}


