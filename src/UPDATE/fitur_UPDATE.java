    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEActionPerformed
                                         
    // Validasi input
    if (jTextField1.getText().trim().isEmpty() || jTextField2.getText().trim().isEmpty() ||
        jTextField3.getText().trim().isEmpty() || jTextField4.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields must be filled!");
        return;
    }

    // Validasi format tanggal
    String purchaseDate = jTextField2.getText().trim();
    if (!isValidDate(purchaseDate)) {
        JOptionPane.showMessageDialog(this, "Invalid Purchase Date format! Correct format: YYYY-MM-DD.");
        return;
    }

    // Siapkan query UPDATE
    String sql = "UPDATE transaksi_beli " +
                 "SET kode_pembelian = ?, " +
                     "tanggal_pembelian = ?, " +
                     "No_Ktp_supplier = ?, " +
                     "Id_barang = ? " +
                 "WHERE kode_pembelian = ?";

    try (Connection conn = DatabaseManager.getConnection();
         PreparedStatement stmt = conn.prepareStatement(sql)) {
        // Set parameter query
        stmt.setString(1, jTextField1.getText().trim());
        stmt.setString(2, purchaseDate);
        stmt.setString(3, jTextField3.getText().trim());
        stmt.setString(4, jTextField4.getText().trim());
        stmt.setString(5, jTextField1.getText().trim()); // Gunakan kode pembelian sebagai kondisi WHERE

        // Eksekusi query
        int result = stmt.executeUpdate();

        // Tangani hasil query
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data updated successfully!");
            tampilkanData();
        } else {
            JOptionPane.showMessageDialog(this, "Data not found or failed to update!");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_btnUPDATEActionPerformed

