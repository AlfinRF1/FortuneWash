    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
                                       
    try {
        String kodePembelian = jTextField1.getText().trim();
        java.util.Date selectedDate = jDateChooser1.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Tanggal Pembelian tidak boleh kosong!");
            return;
        }
        String tanggalPembelian = new SimpleDateFormat("yyyy-MM-dd").format(selectedDate);
        String jumlahPembelianStr = jTextField2.getText().trim();
        String totalHargaStr = jTextField3.getText().trim();
        String hargaBarangStr = jTextField5.getText().trim(); 
        String noKtpSupplier = jComboBox1.getSelectedItem().toString().trim(); 
        String idBarang = jTextField4.getText().trim();

        if (kodePembelian.isEmpty() || tanggalPembelian.isEmpty() || jumlahPembelianStr.isEmpty() || 
            totalHargaStr.isEmpty() || hargaBarangStr.isEmpty() || noKtpSupplier.isEmpty() || idBarang.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }
        int jumlahPembelian = Integer.parseInt(jumlahPembelianStr);
        double totalHarga = Double.parseDouble(totalHargaStr);
        double hargaBarang = Double.parseDouble(hargaBarangStr);

        if (jumlahPembelian <= 0 || totalHarga <= 0 || hargaBarang <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah pembelian, Harga barang, dan Total harga harus lebih dari 0!");
            return;
        }

        String sql = "INSERT IGNORE INTO transaksi_beli (kode_pembelian, tanggal_pembelian, Jumlah_pembelian, harga_barang, total_harga, No_Ktp_supplier, Id_barang) " +
                     "VALUES (?, ?, ?, ?, ?, ?, ?)";

        try (Connection conn = DatabaseManager.getConnection();
             PreparedStatement pst = conn.prepareStatement(sql)) {
            pst.setString(1, kodePembelian);
            pst.setDate(2, new java.sql.Date(selectedDate.getTime()));
            pst.setInt(3, jumlahPembelian);
            pst.setDouble(4, hargaBarang);  // Set the item price value
            pst.setDouble(5, totalHarga);
            pst.setString(6, noKtpSupplier);
            pst.setString(7, idBarang);

            int result = pst.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                tampilkanData(); 
                transaksibeli b = new transaksibeli();
                b.setVisible(true);
                this.dispose();
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error database: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }


    
    }//GEN-LAST:event_btntambahActionPerformed

