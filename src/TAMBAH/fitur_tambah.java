    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
try {
        int selectedRowIndex = jTable1.getSelectedRow();
        if (selectedRowIndex != -1) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String kodePembelian = model.getValueAt(selectedRowIndex, 0).toString();
            String tanggalPembelian = model.getValueAt(selectedRowIndex, 1).toString();
            String jumlahPembelian = model.getValueAt(selectedRowIndex, 2).toString();
            String totalHarga = model.getValueAt(selectedRowIndex, 3).toString();
            String noKtpSupplier = model.getValueAt(selectedRowIndex, 4).toString();
            String idBarang = model.getValueAt(selectedRowIndex, 5).toString();
            
            jTextField1.setText(kodePembelian);
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date date = sdf.parse(tanggalPembelian);
            jDateChooser1.setDate(date);
            jTextField2.setText(jumlahPembelian);
            jTextField3.setText(totalHarga);
           jComboBox1.getSelectedItem().toString().trim(); 
            jTextField4.setText(idBarang);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, 
            "Error saat memuat data: " + e.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jTable1MouseClicked

