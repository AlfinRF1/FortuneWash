    private void HAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HAPUSActionPerformed
       int row = jTable1.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Silahkan pilih data yang akan dihapus", 
            "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    String kodePembelian = jTable1.getValueAt(row, 0).toString();

    int confirm = JOptionPane.showConfirmDialog(this,
        "Apakah anda yakin ingin menghapus data pembelian dengan kode " + kodePembelian + "?",
        "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try (Connection conn = koneksi.koneksiDB();
            PreparedStatement stmt = conn.prepareStatement(
                "DELETE FROM transaksi_beli WHERE kode_pembelian = ?")) {
            stmt.setString(1, kodePembelian);

            int result = stmt.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this,
                    "Data berhasil dihapus!",
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);
                tampilkanData();
            } else {
                JOptionPane.showMessageDialog(this,
                    "Gagal menghapus data", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Error menghapus data: " + e.getMessage(),
                "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_HAPUSActionPerformed

