    private void SEARCHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEARCHActionPerformed
        String searchKeyword = SEARCH.getText().trim();
if (!searchKeyword.isEmpty()) {
    try (Connection conn = koneksi.koneksiDB()) {
        String sql = "SELECT * FROM transaksi_beli " +
                     "WHERE Id_Barang LIKE ? OR " +
                           "kode_pembelian LIKE ? OR " +
                           "tanggal_pembelian LIKE ? OR " +
                           "Jumlah_pembelian LIKE ? OR " +
                           "total_harga LIKE ? OR " +
                           "No_Ktp_supplier LIKE ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, "%" + searchKeyword + "%");
        stmt.setString(2, "%" + searchKeyword + "%");
        stmt.setString(3, "%" + searchKeyword + "%");
        stmt.setString(4, "%" + searchKeyword + "%");
        stmt.setString(5, "%" + searchKeyword + "%");
        stmt.setString(6, "%" + searchKeyword + "%");
        ResultSet rs = stmt.executeQuery();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        while (rs.next()) {
            String idBarang = rs.getString("Id_Barang");
            String kodePembelian = rs.getString("kode_pembelian");
            String tanggalPembelian = rs.getString("tanggal_pembelian");
            int jumlahPembelian = rs.getInt("Jumlah_pembelian");
            double totalHarga = rs.getDouble("total_harga");
            String noKtpSupplier = rs.getString("No_Ktp_supplier");
            model.addRow(new Object[]{idBarang, kodePembelian, tanggalPembelian, jumlahPembelian, totalHarga, noKtpSupplier});
        }
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan", "Pencarian Kosong", JOptionPane.INFORMATION_MESSAGE);
        }
        rs.close();
        stmt.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error during search: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
} else {
    JOptionPane.showMessageDialog(this, "Masukkan nama barang untuk pencarian!", "Peringatan", JOptionPane.WARNING_MESSAGE);
}
    }//GEN-LAST:event_SEARCHActionPerformed

